drop table if exists SoapUITestCase;
drop table if exists SoapUITestsuite;
drop table if exists SoapUITestrun;

create table SoapUITestCase (
	id bigint generated by default as identity,
	name varchar(255) not null,
	status_State varchar(64),
	Reason varchar(255),
	duration bigint,
	operate_Date timestamp,
	operate_User varchar(255),
	
	testsuite_id bigint,
    primary key (id)
);

create table SoapUITestsuite (
	id bigint generated by default as identity,
	name varchar(255) not null,
	status_State varchar(64),
	operate_Date timestamp,
	operate_User varchar(255),
	testrun_id bigint,
    primary key (id)
);

create table SoapUITestrun (
	id bigint generated by default as identity,
	status_State varchar(64),
	operate_Date timestamp,
	operate_User varchar(255),
    primary key (id)
);
